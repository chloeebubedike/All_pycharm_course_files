#String to number
print(int("42"))

#Object to string
print(str(42))

#Object to string
print(repr(43))

#Number of characters
print(len("Chloe"))

#Convert to lowercase
print("Hello".lower())

#Replace a substring
print("Hello everybody".replace('Hello', 'Goodbye'))

#Remove trailing chars
print("Hello everybody in the world     ".rstrip())

#Search for substring
offset = "My favourite food is mac and cheese".find("cheese")
print(offset)