#Use of min, max and sum functions on a numeric list (sorts in ascending numeric order)
myn = [45, 66, 12, 3, 99, 3.142, 42]
print("min:", min(myn), "max:", max(myn))
print("sum:", sum(myn))

#Use of min and max functions on a string dictionary keys (sorts in alphabetic order)
myd = {'fred': 3, 'jim': 8, 'dave': 42}
print("min:", min(myd), "max:", max(myd))